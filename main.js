var config = {
  refreshTime: 250, // In ms
  random: true, // If true -> Set all cells to random, if false -> Use startingArray
  alive: {
    min: 2,
    max: 3
  },
  dead: {
    min: 3,
    max: 3
  }
}

// Game

// If config.random is true -> Set all cells to 0 or 1 using Math.round() and Math.random()

if (config.random) {
  for (var y = 0; y < startingArray.length; y++) {
    for (var x = 0; x < startingArray[y].length; x++) {
      startingArray[y][x] = Math.round( Math.random() );
    }
  }
}

var cells = []; // Create cells array for future use.

var cell = { // Object with functions for cells
  checkNeighbours: function(state, x, y) {
    var numberOfAliveNeighbours = 0;

    if (y > 0) {
      if (cells[y-1][x-1] == 1) {
        numberOfAliveNeighbours += 1;
      }
      if (cells[y-1][x] == 1) {
        numberOfAliveNeighbours += 1;
      }
      if (cells[y-1][x+1] == 1) {
        numberOfAliveNeighbours += 1;
      }
    }

    if (cells[y][x-1] == 1) {
      numberOfAliveNeighbours += 1;
    }
    if (cells[y][x+1] == 1) {
      numberOfAliveNeighbours += 1;
    }

    if (y < cells.length-1) {
      if (cells[y+1][x-1] == 1) {
        numberOfAliveNeighbours += 1;
      }
      if (cells[y+1][x] == 1) {
        numberOfAliveNeighbours += 1;
      }
      if (cells[y+1][x+1] == 1) {
        numberOfAliveNeighbours += 1;
      }
    }

    // State 1 -> Cell is currently alive
    // State 0 -> Cell is currently dead

    if (state === 1) {
      if (numberOfAliveNeighbours >= config.alive.min && numberOfAliveNeighbours <= config.alive.max) {
        return 1; // Stay alive
      } else {
        return 0; // Die of (over/under)population
      }
    } else {
      if (numberOfAliveNeighbours >= config.dead.min && numberOfAliveNeighbours <= config.dead.max) {
        return 1; // Spawn new cell by reproduction
      } else {
        return 0; // Still dead
      }
    }
  }
}

// Copy startingArray to cells array using JSON.stringify and JSON.parse.
// It's the quickest and easiest way to copy array with array inside.
// array.splice() and similiar do NOT copy 2D+ arrays.

var cells = JSON.parse(JSON.stringify(startingArray));

// Set c to canvas and ctx to canvas context

var c = document.getElementById("canvas");
var ctx = c.getContext("2d");

// Animate function is fired every config.refreshTime and is used to refresh the screen.

function animate() {
  ctx.clearRect(0, 0, 800, 600); // Clear the canvas

  var modifiedCells = JSON.parse(JSON.stringify(cells)); // Copy cells Array for this simulation frame

  for (var y = 0; y < cells.length; y++) {
    for (var x = 0; x < cells[y].length; x++) {
      if (cells[y][x] === 1) { // If cell is full make it red
        ctx.fillStyle = "#990000";
        ctx.strokeStyle = "#990000";
        ctx.fillRect(x*16, y*16, 16, 16);
        ctx.strokeRect(x*16, y*16, 16, 16);
      } else { // If cell is empty make it white with black border
        ctx.fillStyle = "#ffffff";
        ctx.strokeStyle = "#000000";
        ctx.fillRect(x*16, y*16, 16, 16);
        ctx.strokeRect(x*16, y*16, 16, 16);
      }
      modifiedCells[y][x] = cell.checkNeighbours(cells[y][x], x, y); // Call neighbour checking function to set cell values
    }
  }

  cells = JSON.parse(JSON.stringify(modifiedCells)); // After simulation is done, copy modified array into the main one

  setTimeout(function () { // Next simulation frame after config.refreshTime
    animate();
  }, config.refreshTime);
}

animate(); // Start simulation
